'''
Jika diawal kalimat pada suatu string yang dimiliki belum berupa huruf kapital maka dengan menggunakan fitur .capitalize() kita dengan mudah merubah string tersebut menjadi kalimat yang benar secara bahasa.

.capitalize() untuk Mengubah elemen pertama dari string menjadi huruf kapital.
.lower() untuk Mengubah seluruh huruf dalam teks (string) menjadi huruf kecil
.upper() untuk Mengubah seluruh huruf dalam teks (string) menjadi huruf besar
'''

# Fitur .capitalize()
print(">>> Fitur .capitalize()")
judul_buku = 'belajar bahasa Python'
print(judul_buku.capitalize())
# Fitur .lower()
print(">>> Fitur .lower()")
judul_buku = 'Belajar Bahasa PYTHON.'
print(judul_buku.lower())
# Fitur .upper()
print(">>> Fitur .upper()")
judul_buku = 'Belajar Bahasa PYTHON.'
print(judul_buku.upper())